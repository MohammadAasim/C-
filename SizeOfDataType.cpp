#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    char ch;
    float f;
    double d;
    short s;
    long l;
    cout<<"size of int : "<<sizeof(n)<<endl;
    cout<<"size of char : "<<sizeof(ch)<<endl;
    cout<<"size of float : "<<sizeof(f)<<endl;
    cout<<"size of double : "<<sizeof(d)<<endl;
    cout<<"size of short : "<<sizeof(s)<<endl;
    cout<<"size of long : "<<sizeof(l)<<endl;

    return 0;
}